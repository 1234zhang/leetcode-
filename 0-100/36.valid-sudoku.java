import java.util.*;
class Solution {
    public boolean isValidSudoku(char[][] board) {
        int max = 9;
        Map<Integer,Integer>[] row = new HashMap[9];
        Map<Integer,Integer>[] column = new HashMap[9];
        Map<Integer,Integer>[] box = new HashMap[9];
        for (int i = 0; i < 9; i++) {
            row[i] = new HashMap<>();
            column[i] = new HashMap<>();
            box[i] = new HashMap<>();
        }
        for (int i = 0; i < max; i++) {
                for (int j = 0; j < max; j++) {
                    if(board[i][j] != '.'){
                    int n = (int)board[i][j];
                    int boxNum = (i/3)*3 + (j/3);
                    
                    row[i].put(n, row[i].getOrDefault(n, 0)+1);
                    column[j].put(n, column[j].getOrDefault(n, 0)+1);
                    box[boxNum].put(n, box[boxNum].getOrDefault(n, 0) + 1);
                    if(row[i].get(n) > 1 || column[j].get(n) > 1 || box[boxNum].get(n) > 1){
                        return false;
                    }
                }
            }
        }
        return true;
    }
}

